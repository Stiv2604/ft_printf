#include "ft_printf.h"

void ft_atoi(char *str, int *t)
{
	int		i;

	i = 0;
	while ((str[i] != '\0') && (str[i] >= 48) && (str[i] <= 57))
	{
		*t = *t * 10 + (str[i] - '0');
		i++;
	}
}